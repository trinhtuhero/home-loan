package vn.com.msb.homeloan.core.constant;

import java.util.Map;
import java.util.stream.Collectors;
import java.util.stream.Stream;

public enum InterestedEnum {

  INTERESTED("INTERESTED", "Quan tâm"),
  NOT_INTERESTED("NOT_INTERESTED", "Không quan tâm");

  private String code;
  private String name;

  InterestedEnum(String code, String name) {
    this.name = name;
    this.code = code;
  }

  public String getName() {
    return name;
  }

  public String getCode() {
    return code;
  }

  public static Map<String, String> toMap = Stream.of(values())
      .collect(Collectors.toMap(k -> k.getCode(), v -> v.getName()));

}
